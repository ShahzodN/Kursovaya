namespace Kursovaya
{
	public enum RoomTypeEnum
	{
		//Single – однокомнатный, для размещения одного отдыхающего. Спальное место одно.
		SGL,

		//Double – однокомнатный номер для двоих. Может быть с одной большой кроватью или с двумя раздельными кроватями (double twin). Условное обозначение – DBL, DBL TWN.
		DBL,
		DBL_TWN,

		//Triple – номер для троих отдыхающих. Обычно в номере две кровати и одно дополнительное спальное место (диван, либо раскладушка).
		TRPL,
	}
}
